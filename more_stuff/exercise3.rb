# Q. What is exception handling and what problem does it solve?
# A. Exception handling is a structure, usually a built-in language feature, programmers use to handle
#    (change the control flow of) possible errors so programs can gracefully respond to problems like
#    unexpected input or results rather than exiting the program entirely.
#
#    Programmers usually handle specific exception types because they don't want the same handler to manage
#    every exception type.
